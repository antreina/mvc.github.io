            <p2>La región de la Sierra del Ecuador va de norte a sur a través de los Andes <br>
            y se sitúa a una latitud entre los 1.800 y 6.310 metros sobre el nivel de mar,<br>
            caracterizada por sus elevadas montañas, elevados volcanes y nevados.<br>
            Es una región formada por 5 provincias: Chimborazo, Imbabura, Cotopaxi,<br>
            Punchicha y Tungurahua. Su clima presenta una temperatura promedia de <br>
            unos 14 ºC a lo largo del año y en cuanto a las precipitaciones presenta <br>
            dos periodos muy marcados: el seco que se da entre los meses de enero a <br>
            mayo y el lluvioso dándose de septiembre a noviembre.</p>

            <p2>Entre la flora de la Sierra del Ecuador destacan las gramíneas,<br>
            árboles cubiertos de musgo, margaritas, girasoles, brezos, flores multicolores <br>
            y frailejones gigantes, entre otras plantas. Sin embargo, en cuanto a la<br>
            fauna de la Sierra del Ecuador, son muy comunes especies como el puma, <br>
            el cóndor, las llamas, la vicuña, los osos y la comadreja andina, entro otros.<br>

            Aquí puedes descubrir mucho más sobre la Región Andina: características, flora y fauna.</p2>

