           <p>Fuera de la zona continental se encuentra la Región Insular o también llamada las Islas Galápagos,<br>
            se ubica en el océano Pacífico a 1.000 km de la costa ecuatoriana. El archipiélago está formado <br>
            por 13 grandes islas volcánicas siendo estas las principales, 6 islas medianas y 40 islotes<br>
            pequeños que han surgido como consecuencia de diferentes erupciones volcánicas. Las principales<br>
            islas son: Isabela, Fernandina, San Cristóbal, Española, Pinta, Pinzón, Baltra, Santa Cruz,<br>
            San Salvador, Floreana, Marchena, Santa Fe, Genovesa y Rábida.</p> 

            <p>Las playas de esta región están caracterizadas por su arena blanca y fina, marcando un gran<br>
            contraste con el agua cristalina del mar y las piedras oscuras. El clima de las Galápagos es<br>
            caluroso desde el mes de diciembre a finales de mayo, la época fría sin embargo empieza<br>
            a partir de junio hasta noviembre.</p> 
            
            <p>En las Galápagos pueden más de 180 especies de especies de animales endémicas y más de 500<br>
            especies de plantas nativas. Entre su fauna pueden encontrarse especies como iguanas      <br>
            terrestres y marinas, tiburones ballena, ballenas azules, piqueros de patas rojas y azules,<br>
            leones marinos, flamencos, pingüinos, entre otros. En cuanto a su flora está el algodón de Darwin,<br>
            especies de arbustos, la margarita endémica, el tomate de Galápagos, el cactus de lava o candelabro,<br>
            entre otras.</p> 
            
